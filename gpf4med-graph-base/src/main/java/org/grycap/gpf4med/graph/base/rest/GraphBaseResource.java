package org.grycap.gpf4med.graph.base.rest;

import javax.ws.rs.GET;
import javax.ws.rs.Path;
import javax.ws.rs.Produces;
import javax.ws.rs.core.MediaType;

import org.grycap.gpf4med.model.util.PingResponse;

/**
 * Graph base REST resource. API version is mandatory in the URL.
 * @author Erik Torres <ertorser@upv.es>
 */
@Path("graph-base/v1")
public interface GraphBaseResource {

	public static final String PATH_STRING = GraphBaseResource.class.getAnnotation(Path.class).value();
	
	@Path("ping")
	@GET
	@Produces(MediaType.APPLICATION_JSON)
	PingResponse ping();
	
}